    <h2>Frequently Asked Questions</h2>
    <h3>How do you calculate a tip?</h3>
    <p>To calculate a tip, multiply the total bill amount by the tip percentage (e.g., 20%). Add the result to the original bill to get the total amount.</p>

    <h3>What is the best tip percentage?</h3>
    <p>Most people tip 15-20% for restaurant service, but this can vary based on the quality of service.</p>

    <h3>Can I split the tip with friends?</h3>
    <p>Yes! Simply enter the number of people splitting the bill, and the tool will calculate the amount each person owes.</p>
